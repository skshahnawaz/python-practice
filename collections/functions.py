# Functions are used to repeatedly run a common block of code.

# Define a function named sum which takes two values, adds two numbers and returns their sum.
def sum(a,b):
    s = a+b # Sum is calculated here
    return s # The sum is returned from the function

def subtract(a,b):
    s = a - b
    return s

print(sum(3,4))
print(subtract(7,3))


